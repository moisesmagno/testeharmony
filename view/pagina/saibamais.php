                            <h3 class="h3_desc_saibamais">Dúvidas e Perguntas</h3>
                            <p class="p_desc_saibamais">
                                <span>O teste Pré-Natal Harmony™ pode ser realizado em todas as grávidas?</span><br> 
                                Sim, a partir da 10ª semana completa de gestação, e agora também pode ser realizado em gravidezes gemelares, por concepção natural ou por FIV, com os óvulos da própria grávida ou de dadora. É necessária prescrição médica.
                                <br> <br>     
                                <span>Qual a Taxa de detecção deste Teste?</span><br> 
                                Os dados disponíveis até à data, incluindo publicações científicas, de estudos com mais de 6000 mulheres mostram taxas de detecção de trissomias 21 e 18 acima de 99% e 98%, respectivamente. Devido ao baixo número de casos de trissomia 13 os dados disponíveis são 8 casos detectados em 10.
                                <br> <br>         
                                <span>Qual é a taxa de falsos positivos e a de falsos negativos?</span><br> 
                                As taxas de falsos positivos para as trissomias 21, 18 e 13 são inferiores a 0,1%, enquanto as taxas de falsos negativos para as trissomias 21, 18 são inferiores a 0,1%.
                                <br> <br>         
                                <span>Uma gravidez anterior interfere no resultado do teste Pré-Natal Harmony™?</span><br> 
                                Não. Embora alguns estudos demonstram que as células do feto podem manter-se em circulação no sangue materno até um ano após o nascimento, o ADN livre fetal, alvo de análise deste teste, é eliminado poucas horas após o parto.
                                <br> <br>         
                                <span>Uma gravidez anterior não de termo (aborto) interfere no resultado teste Pré-Natal Harmony™?</span><br> 
                                Não. Embora alguns estudos demonstram que as células do feto podem manter-se em circulação no sangue materno até um ano após o nascimento, o ADN livre fetal, alvo de análise deste teste, é eliminado poucas horas após o parto.
                                <br> <br>         
                                <span>O teste Pré-Natal Harmony™ detecta mosaicismos, translocações ou outras anomalias cromossómicas?</span><br> 
                                Não. O teste foi desenvolvido para a determinação do risco de um feto apresentar umas das trissomias 21, 18 ou 13. Com os novos estudos realizados em simultâneo com o teste Pré-Natal Harmony, são determinados os riscos para a presença de aneuploidias dos cromossomas X e Y.
                                <br> <br>         
                                <span>A partir de que semana se pode realizar o teste Pré-Natal Harmony™?</span><br> 
                                A grávida deve completar 10 semanas de gestação.
                                <br> <br>         
                                <span>Pode ser realizada em grávidas com tratamento de FIV?</span><br> 
                                Sim, incluindo grávidas submetidas a tratamentos com óvulos doados, em gravidez de feto único e agora também em gravidez gemelar (até dois fetos).
                                <br> <br>         
                                <span>O teste Pré-Natal Harmony™ detecta o sexo fetal?</span><br> 
                                Sim. Em simultâneo com o estudo das trissomias 13, 18 e 21, podem ser estudadas as aneuploidias dos cromossomas X e Y e determinação do sexo fetal, bastando para isso serem requisitados pelo seu médico assistente.
                                <br> <br>         
                                <span>A determinação do sexo fetal e o estudo das aneuploidias dos cromossomas X e Y pode ser realizado em gravidezes gemelares?</span><br> 
                                Não, apenas em gravidezes de feto único.
                                <br> <br>         
                                <span>A determinação do sexo fetal e o estudo das aneuploidias dos cromossomas X e Y pode ser realizado em grávidas com tratamento FIV?</span><br> 
                                Sim, quer tenha sido realizado com os óvulos da própria grávida, quer com os óvulo de uma dadora.
                                <br> <br>     
                                <span>Critérios de Elegibilidade para a realização dos testes Pré-Natal Harmony, Detecção Sexo Fetal e Aneuploidias X e Y.</span><br> 
                                <span>Harmony:</span> Todas as gravidezes de feto único ou gemelares até dois fetos, incluindo por FIV.
                                <span>Harmony com Análise do cromossoma Y ou dos cromossomas X, Y:</span> Apenas gravidezes de feto único de concepção natural ou por FIV.
                                <br> <br>         
                                <span>Quais as condições a ter em conta para a colheita da amostra?</span><br> 
                                A grávida não necessita de estar em jejum.
                                O que tem de ter em conta é o horário das colheitas no laboratório ou hospital onde irá realizar esta colheita.
                                <br> <br>     
                                <span>O teste Pré-Natal Harmony™ é comparticipado por alguma Convenção ou Seguradora?</span><br> 
                                Actualmente não.
                                <br> <br>     
                                <span>Qual é o prazo de entrega dos resultados?</span><br> 
                                O prazo máximo de entrega dos resultados é de 15 dias.
                                <br> <br>     
                                <span>Como é apresentado o resultado?</span><br> 
                                O resultado é apresentado em percentagem de risco que varia entre 0,01% e 99%. Ou seja, uma grávida com um risco de 0,01% terá um risco de 1 em cada 10000 casos, e uma grávida com um risco de 99% terá um risco de 99 em cada 100 casos.
                                <br> <br>     
                                <span>Obtém-se sempre um resultado ou existe a possibilidade de não se obter resultado?</span><br> 
                                Para poder realizar este teste, é necessário obter no mínimo 4% de ADN livre fetal.
                                As amostras que contenham menos quantidade não podem ser analisadas, não se obtendo resultado. Esta situação ocorre em 3% de amostras.
                                Neste caso, é repetida a colheita sem custos adicionais.
                            </p>
