                                </li> 
                                <li>
                                    <span>Amniocentese:</span> consiste na aspiração de uma pequena quantidade de líquido amniótico que envolve o feto, para análise dos cromossomas. A amniocentese deve ser realizada por volta das 16 semanas de gravidez.
