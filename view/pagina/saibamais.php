                            <h3 class="h3_desc_saibamais">O que é uma Trissomia?</h3>
                            <p class="p_desc_saibamais">
                                    Os seres humanos têm 23 pares de cromossomas, constituídos por ADN e proteínas que transportam 
                                    a informação genética.
                                    <br><br>
                                    A trissomia é uma anomalia cromossómica que se deve à presença de três cópias de um cromossoma, em vez das habituais duas.
                                    <br><br>    
                                    <span>A trissomia 21 ou Síndrome de Down</span> é a mais frequente. A Síndrome de Down está associada a atraso mental leve a moderado, pode ainda causar defeitos cardíacos congénitos, entre outras malformações. Na ausência de qualquer tipo de rastreio, calcula-se que aproximadamente 1 em cada 700 bebés nascerá com trissomia 21.
                                    <br><br>    
                                    <span>A trissomia 13 ou Síndrome de Patau</span> é mais rara, estando associada a uma taxa elevada de aborto espontâneo. Os bebés nascidos com esta trissomia têm frequentemente malformações congénitas graves e uma esperança de vida reduzida. Na ausência de qualquer tipo de rastreio, calcula-se que aproximadamente 1 em cada 16.000 bebés nascerá com trissomia 13.
                                    <br><br>    
                                    <span>A trissomia 18 ou Síndrome de Edwards</span> é também mais rara, estando igualmente associada a uma elevada taxa de aborto espontâneo. Os bebés com esta trissomia têm frequentemente malformações congénitas graves e uma esperança de vida reduzida. Na ausência de qualquer tipo de rastreio, calcula-se que aproximadamente  1 em cada 5.000 bebés nascerá com trissomia 18.
